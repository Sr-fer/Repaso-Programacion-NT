function datos_usuario() {
    var recopilador_datos = []
    var b = parseInt(prompt("Elige un numero para b"))
    var a = parseInt(prompt("Elige un numero para a"))
    var c = parseInt(prompt("Elige un numero para c"))
    recopilador_datos.push(b)
    recopilador_datos.push(a)
    recopilador_datos.push(c)
    return recopilador_datos
}